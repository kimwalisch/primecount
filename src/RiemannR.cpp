/// Calculate an initial nth prime approximation using Cesàro's formula.
/// Cesàro, Ernesto (1894). "Sur une formule empirique de M. Pervouchine". Comptes
/// Rendus Hebdomadaires des Séances de l'Académie des Sciences. 119: 848–849.
/// https://en.wikipedia.org/wiki/Prime_number_theorem#Approximations_for_the_nth_prime_number
///
template <typename T>
T initialNthPrimeApprox(T x)
{
  if (x < 1)
    return 0;
  else if (x >= 1 && x < 2)
    return 2;
  else if (x >= 2 && x < 3)
    return 3;

  T logx = std::log(x);
  T loglogx = std::log(logx);
  T t = logx + (loglogx / 2);

  if (x > 1600)
    t += (loglogx / 2) - 1 + (loglogx - 2) / logx;
  if (x > 1200000)
    t -= (loglogx * loglogx - 6 * loglogx + 11) / (2 * logx * logx);

  return x * t;
}

/// Calculate the Riemann R function which is a very
/// accurate approximation of the number of primes <= x.
/// http://mathworld.wolfram.com/RiemannPrimeCountingFunction.html
/// The calculation is done with the Gram series:
/// RiemannR(x) = 1 + \sum_{k=1}^{∞} ln(x)^k / (zeta(k + 1) * k * k!)
///
template <typename T>
T RiemannR(T x)
{
  if (x < T(1e-5))
    return 0;

  T epsilon = pstd::numeric_limits<T>::epsilon();
  T sum = 1;
  T term = 1;
  T logx = std::log(x);

  // The condition k < ITERS is required in case the computation
  // does not converge. This happened on Linux i386 where
  // the precision of the libc math functions is very limited.
  for (unsigned k = 1; k < 1000; k++)
  {
    term *= logx / k;
    T old_sum = sum;

    if (k + 1 < zeta.size())
      sum += term / (T(zeta[k + 1]) * k);
    else
      // For k >= 127, approximate zeta(k + 1) by 1
      sum += term / k;

    // Not converging anymore
    if (std::abs(sum - old_sum) <= epsilon)
      break;
  }

  return sum;
}

/// Calculate the inverse Riemann R function which is a
/// very accurate approximation of the nth prime.
/// This implementation computes RiemannR^-1(x) = t as the zero of the
/// function f(t) = RiemannR(t) - x using the Newton–Raphson method.
/// https://en.wikipedia.org/wiki/Newton%27s_method
///
template <typename T>
T RiemannR_inverse(T x)
{
  if (x < 1)
    return 0;

  T t = initialNthPrimeApprox(x);
  T old_term = pstd::numeric_limits<T>::infinity();

  // The condition i < ITERS is required in case the computation
  // does not converge. This happened on Linux i386 where
  // the precision of the libc math functions is very limited.
  for (int i = 0; i < 10; i++)
  {
    // term = f(t) / f'(t)
    // f(t) = RiemannR(t) - x
    // RiemannR(t) ~ li(t), hence f'(t) = li'(t) = 1 / log(t)
    // term = (RiemannR(t) - x) / li'(t) = (RiemannR(t) - x) * log(t)
    T term = (RiemannR(t) - x) * std::log(t);

    // Not converging anymore
    if (std::abs(term) >= std::abs(old_term))
      break;

    t -= term;
    old_term = term;
  }

  return t;
}

/// Approximate Chebyshev's psi(x) using a truncated form of
/// the Riemann-von Mangoldt explicit formula for psi(x)
/// with the first 512 positive imaginary parts gamma of the
/// non-trivial zeta zeros.
///
template <typename T>
T ChebyshevPsiApprox(T x)
{
  if (x <= 1)
    return x;

  T logx = std::log(x);
  T sqrtx = std::sqrt(x);
  T sum = 0;

  for (long double gamma_ : zeta_zero_gammas)
  {
    T gamma = (T) gamma_;
    T angle = gamma * logx;
    T denom = 0.25 + gamma * gamma;
    sum += ((0.5 * std::cos(angle)) + gamma * std::sin(angle)) / denom;
  }

  T log_2pi = (T) 1.837877066409345483560659472811235L;
  T psi = x - (2 * sqrtx * sum) - log_2pi;
  T xx = x * x;

  psi -= 0.5 * std::log(1 - (1 / xx));

  return psi;
}

/// RiemannR(psi(x)) is even more accurate than RiemannR(x)
/// for estimating the number of primes <= x.
/// Planat, M., & Solé, P. (2014). "Improving Riemann prime counting."
/// arXiv. https://doi.org/10.48550/arXiv.1410.1083.
///
template <typename T>
T RiemannR_psi(T x)
{
  if (x < (T) 1e4)
    return RiemannR(x);
  else
    return RiemannR(ChebyshevPsiApprox(x));
}

/// Calculate the inverse of RiemannR(psi(x)) which is
/// a very accurate approximation of the nth prime.
/// This implementation uses the Newton–Raphson method.
/// https://en.wikipedia.org/wiki/Newton%27s_method
///
template <typename T>
T RiemannR_psi_inverse(T x)
{
  if (x < 1)
    return 0;

  T t = initialNthPrimeApprox(x);
  T old_term = pstd::numeric_limits<T>::infinity();

  // The condition i < ITERS is required in case the computation
  // does not converge. This happened on Linux i386 where
  // the precision of the libc math functions is very limited.
  for (int i = 0; i < 10; i++)
  {
    // term = f(t) / f'(t)
    // f(t) = RiemannR(psi(t)) - x
    // RiemannR(psi(t)) ~ li(t), hence f'(t) = li'(t) = 1 / log(t)
    // term = (RiemannR(psi(t)) - x) / li'(t) = (RiemannR(psi(t)) - x) * log(t)
    T term = (RiemannR_psi(t) - x) * std::log(t);

    // Not converging anymore
    if (std::abs(term) >= std::abs(old_term))
      break;

    t -= term;
    old_term = term;
  }

  return t;
}

